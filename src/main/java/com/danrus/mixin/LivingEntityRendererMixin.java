@Mixin(LivingEntityRenderer.class)
public class LivingEntityRendererMixin <S extends LivingEntityRenderState> {
    @WrapOperation(
            method = "render(Lnet/minecraft/client/render/entity/state/LivingEntityRenderState;Lnet/minecraft/client/util/math/MatrixStack;Lnet/minecraft/client/render/VertexConsumerProvider;I)V",
            at = @At(value = "INVOKE", target = "Lnet/minecraft/client/render/entity/model/EntityModel;render(Lnet/minecraft/client/util/math/MatrixStack;Lnet/minecraft/client/render/VertexConsumer;III)V")
    )
    private void render(EntityModel instance, MatrixStack matrixStack, VertexConsumer vertexConsumer, int i1, int i2, int i3, Operation<Void> original){
            RenderSystem.enableBlend();
            RenderSystem.defaultBlendFunc();
            original.call(instance, matrixStack, vertexConsumer, i1, i2, i3);
            RenderSystem.disableBlend();
    }
}
